# арифметичні операції з числами

print(7 + 3)  # додавання
print(10 - 4)  # віднімання

print(5 * 2)  # множення
print(10 / 4)  # ділення дробове
print(8 * 2)
print(5 / 2) 

print(16.0 / 8) 
print(16 / 8.0) 
print(16.0 / 8.0) 

print(167 % 15)  # остача від ділення
print(167 // 15)  # цілочисельне ділення

print(3 ** 2)  # піднесення до степеня
